    echo "âœ… Keycloak admin secret already exists. Skipping generation."
else
    echo "ðŸ” Generating Keycloak admin credentials..."
    ADMIN_PASS=$(openssl rand -base64 24)
    bao_exec kv put secret/keycloak/admin \
        username="admin" \
        password="$ADMIN_PASS"
    echo "âœ… Keycloak admin credentials stored."
    echo "   âš  Save the admin password securely â€” it won't be displayed again."
